import { prefixPreservingIncrement } from '../src/util/util'

test('prefixPreservingIncrement', () => {
    expect(prefixPreservingIncrement(1, 1)).toBe(10)
    expect(prefixPreservingIncrement(18, 1)).toBe(19)
    expect(prefixPreservingIncrement(19, 1)).toBe(100)
    expect(prefixPreservingIncrement(20, 1)).toBe(100)
    expect(prefixPreservingIncrement(99, 1)).toBe(100)
    expect(prefixPreservingIncrement(100, 1)).toBe(101)
    expect(prefixPreservingIncrement(101, 1)).toBe(102)
    expect(prefixPreservingIncrement(200, 1)).toBe(1000)
    expect(prefixPreservingIncrement(1000, 1)).toBe(1001)
    expect(prefixPreservingIncrement(1001, 1)).toBe(1002)
    expect(prefixPreservingIncrement(2000, 1)).toBe(10000)
    expect(prefixPreservingIncrement(10000, 1)).toBe(10001)

    expect(prefixPreservingIncrement(1, 2)).toBe(2)
    expect(prefixPreservingIncrement(2, 2)).toBe(20)
    expect(prefixPreservingIncrement(19, 2)).toBe(20)
    expect(prefixPreservingIncrement(19, 2)).toBe(20)
    expect(prefixPreservingIncrement(20, 2)).toBe(21)
    expect(prefixPreservingIncrement(21, 2)).toBe(22)
    expect(prefixPreservingIncrement(29, 2)).toBe(200)
    expect(prefixPreservingIncrement(200, 2)).toBe(201)
    expect(prefixPreservingIncrement(201, 2)).toBe(202)
    expect(prefixPreservingIncrement(300, 2)).toBe(2000)
    expect(prefixPreservingIncrement(2000, 2)).toBe(2001)
    expect(prefixPreservingIncrement(2001, 2)).toBe(2002)
    expect(prefixPreservingIncrement(3000, 2)).toBe(20000)
    expect(prefixPreservingIncrement(20000, 2)).toBe(20001)
})
